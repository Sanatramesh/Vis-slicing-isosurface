// Interpolate to find point P corresponding to salanity value(sal). 
Vertex* Edge::interpolateP(float sal){
	Vertex *v = new Vertex();
	v->setSalinity(sal);
	v->setX(vertex1->getX());
	v->setY(vertex1->getY());
	v->setZ(vertex1->getZ());
	int val = 0;
	if (vertex1->getX() != vertex2->getX()){
		val = vertex2->getX() + (vertex1->getX() - vertex2->getX())*(sal - vertex2->getSalinity())/(vertex1->getSalinity() - vertex2->getSalinity());
		v->setX(val);		
	}else if(vertex1->getY() != vertex2->getY()){
		val = vertex2->getY() + (vertex1->getY() - vertex2->getY())*(sal - vertex2->getSalinity())/(vertex1->getSalinity() - vertex2->getSalinity());
		v->setY(val);
	}else{
		val = vertex2->getZ() + (vertex1->getZ() - vertex2->getZ())*(sal - vertex2->getSalinity())/(vertex1->getSalinity() - vertex2->getSalinity());
		v->setZ(val);
	}
	
	return v;
}
// Interpolate to find point of intersection of the edge and the plane(a, b, c, d). 
Vertex* Edge::interpolateSalinity(float a,float b,float c,float d){
	float sal;
	float dx = vertex2->getX() - vertex1->getX();
	float dy = vertex2->getY() - vertex1->getY();
	float dz = vertex2->getZ() - vertex1->getZ();
	float dsal = vertex2->getSalinity() - vertex1->getSalinity();
	float dist = vertex1->getX()*a + vertex1->getY()*b + vertex1->getZ()*c + d;
	
	float t = -dist/(a*dx + b*dy + c*dz);
	
	Vertex *v = new Vertex();
	v->setX(vertex1->getX() + t*dx);
	v->setY(vertex1->getY() + t*dy);
	v->setZ(vertex1->getZ() + t*dz);
	v->setSalinity(vertex1->getSalinity());
	
	if (vertex1->getX() != vertex2->getX()){
		sal = vertex1->getSalinity() + dsal*(v->getX() - vertex1->getX())/dx;
		v->setSalinity(sal);		
	}else if(vertex1->getY() != vertex2->getY()){
		sal = vertex1->getSalinity() + dsal*(v->getY() - vertex1->getY())/dy;
		v->setSalinity(sal);
	}else{
		sal = vertex1->getSalinity() + dsal*(v->getZ() - vertex1->getZ())/dz;
		v->setSalinity(sal);
	}
	return v;
}

